def red(text: str):
    print('\033[91m' + text + '\033[0m')

def yellow(text: str):
    print('\033[93m' + text + '\033[0m')

def green(text: str):
    print('\033[92m' + text + '\033[0m')

def lightblue(text: str):
    print('\033[96m' + text + '\033[0m')

def blue(text: str):
    print('\033[94m' + text + '\033[0m')

def grey(text: str):
    print('\033[90m' + text + '\033[0m')
