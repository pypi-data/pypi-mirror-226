"""Simple arithmetic demo."""


def add_ints(number1: int, number2: int) -> int:
    """Adds two numbers.

    Args:
        number1: The first number.
        number2: The second number.

    Returns:
        The two numbers added together.


    For example.

    >>> add_ints(1,1)
    2
    >>> add_ints(-2,2)
    0

    """
    return number1 + number2
